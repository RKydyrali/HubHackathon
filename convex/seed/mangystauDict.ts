export function weightedPick<T extends { w: number }>(rng: () => number, items: T[]): T {
  const total = items.reduce((s, i) => s + i.w, 0);
  let r = rng() * total;
  for (const item of items) {
    r -= item.w;
    if (r <= 0) return item;
  }
  return items[items.length - 1]!;
}

export function pick<T>(rng: () => number, arr: readonly T[]): T {
  return arr[Math.floor(rng() * arr.length)]!;
}

/** Жилой «район» в профиле: для Актау — микрорайон; для остальных городов региона — нейтральная зона. */
export function pickResidentialAreaForCity(
  rng: () => number,
  city: (typeof CITIES)[number],
): string | undefined {
  if (city === "Актау") {
    return rng() < 0.94 ? pick(rng, AKTAU_MICRODISTRICTS) : undefined;
  }
  return rng() < 0.4 ? pick(rng, REGION_AREA_LABELS) : undefined;
}

/** Юридический / почтовый адрес компании: в Актау — мкр. + дом; в других населённых пунктах — улица из справочника + дом. */
export function formatCompanyAddress(
  rng: () => number,
  city: (typeof CITIES)[number],
): { line: string; district?: string } {
  const house = 5 + Math.floor(rng() * 120);
  if (city === "Актау") {
    const mkr = pick(rng, AKTAU_MICRODISTRICTS);
    return { line: `Актау, ${mkr}, д. ${house}`, district: mkr };
  }
  const street = pick(rng, STREETS);
  const district = rng() < 0.48 ? pick(rng, REGION_AREA_LABELS) : undefined;
  return {
    line: district ? `${city}, ${district}, ${street}, д. ${house}` : `${city}, ${street}, д. ${house}`,
    district,
  };
}

/**
 * Локация вакансии. Для **Актау** всегда задаётся микрорайон (как в реальной адресации города).
 */
export function pickVacancyMicrodistrictOrArea(
  rng: () => number,
  company: { city: string; district?: string | undefined },
): string | undefined {
  const city = company.city as (typeof CITIES)[number];
  if (!(CITIES as readonly string[]).includes(company.city)) {
    return rng() < 0.55 ? company.district : undefined;
  }
  if (city === "Актау") {
    if (company.district && rng() < 0.58) {
      return company.district;
    }
    return pick(rng, AKTAU_MICRODISTRICTS);
  }
  if (rng() < 0.45 && company.district) {
    return company.district;
  }
  return pickResidentialAreaForCity(rng, city);
}
